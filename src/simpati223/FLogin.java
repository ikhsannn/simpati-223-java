    public void Login(){
        String jUser = jTextField1.getText();
        String jPass = String.valueOf(jPasswordField2.getPassword()) ;
        try{
            String sqlVUser = "select * from users where username = '" + jUser + "' and password = '" +
                    jPass + "'";
            Statement stmt = conn.createStatement();
            ResultSet rs =  stmt.executeQuery(sqlVUser);
            int brs = 0;
            while (rs.next()){
               brs = rs.getRow();
            }
            
            if (brs == 1){
               // JOptionPane.showMessageDialog(null, "Login sukses...");
               FMenu menu = new FMenu();
               menu.setVisible(true);
               menu.setTitle("Form Menu");
               menu.setExtendedState(JFrame.MAXIMIZED_BOTH);
               this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "User ID tidak ada...");
            }
            stmt.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Login gagal..." + e.getMessage());
        }
    }
