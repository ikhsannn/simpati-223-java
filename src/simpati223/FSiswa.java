   public static void lockJTableColumnWidth(TableColumn tc, int pixels) {
       if (tc != null) {
           tc.setMinWidth(pixels);
           tc.setMaxWidth(pixels);
           tc.setPreferredWidth(pixels);
           tc.setResizable(false);
       }
   }
   
       public void TampilData() {
        DftTblModel_Siswa = new DefaultTableModel();
        DftTblModel_Siswa.addColumn("id");
        DftTblModel_Siswa.addColumn("NIPD"); //tampilin ke layar
        DftTblModel_Siswa.addColumn("NISN"); //tampilin ke layar
        DftTblModel_Siswa.addColumn("Nama"); //tampilin ke layar
        DftTblModel_Siswa.addColumn("Kelas");//tampilin ke layar
        DftTblModel_Siswa.addColumn("Jenis Kelamin");
        DftTblModel_Siswa.addColumn("Agama");
        DftTblModel_Siswa.addColumn("Tahun Ajaran"); //tampilin ke layar
        DftTblModel_Siswa.addColumn("ABK Status");
        DftTblModel_Siswa.addColumn("Tempat Lahir");
        DftTblModel_Siswa.addColumn("Tanggal Lahir");
        DftTblModel_Siswa.addColumn("Alamat");
        jTable1.setModel(DftTblModel_Siswa);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from siswa";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_Siswa.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nipd"), //tampilin ke layar
                    res.getString("nisn"), //tampilin ke layar
                    res.getString("nama"), //tampilin ke layar
                    res.getString("kelas"), //tampilin ke layar
                    res.getString("jenis_kelamin"),
                    res.getString("agama"),
                    res.getString("tahun_ajaran"),//tampilin ke layar
                    res.getString("abk_status"),
                    res.getString("tempat_lahir"),
                    res.getString("tgl_lahir"),
                    res.getString("alamat"),
                });
            }
            hideJTableColumn(jTable1.getColumnModel().getColumn(0));
            hideJTableColumn(jTable1.getColumnModel().getColumn(5));
            hideJTableColumn(jTable1.getColumnModel().getColumn(6));
            hideJTableColumn(jTable1.getColumnModel().getColumn(8));
            hideJTableColumn(jTable1.getColumnModel().getColumn(9));
            hideJTableColumn(jTable1.getColumnModel().getColumn(10));
            hideJTableColumn(jTable1.getColumnModel().getColumn(11));
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
