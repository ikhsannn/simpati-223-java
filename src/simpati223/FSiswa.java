    public void simpanData(){
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("insert into siswa(nipd, nisn, nama, kelas, jenis_kelamin, agama, tahun_ajaran, abk_status, tempat_lahir, tgl_lahir, alamat) "
                    + "values(?,?,?,?,?,?,?,?,?,?,?)");
            stmt.setString(1, jTextField3.getText());
            stmt.setString(2, jTextField4.getText());
            stmt.setString(3, jTextField5.getText());
            stmt.setString(4, jComboBox2.getItemAt(jComboBox2.getSelectedIndex()));
            stmt.setString(5, jComboBox3.getItemAt(jComboBox3.getSelectedIndex()));
            stmt.setString(6, jComboBox4.getItemAt(jComboBox4.getSelectedIndex()));
            stmt.setString(7, jTextField8.getText());
            stmt.setString(8, jComboBox5.getItemAt(jComboBox5.getSelectedIndex()));
            stmt.setString(9, jTextField10.getText());      
            stmt.setObject(10, jDateChooser1.getDate());
            stmt.setString(11, jTextArea1.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            clearData();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
